/**
 * add - add two numbers.
 *
 * @a: num1
 * @b: num2
 *
 * Return: num1 + num2
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers.
 *
 * @a: num1
 * @b: num2
 *
 * Return: num1 - num2
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers.
 *
 * @a: num1
 * @b: num2
 *
 * Return: num1 * num2
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers.
 *
 * @a: num1
 * @b: num2
 *
 * Return: num1 / num2
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modules a number.
 *
 * @a: num1
 * @b: num2
 *
 * Return: num1 % num2
 */
int mod(int a, int b)
{
	return (a % b);
}
